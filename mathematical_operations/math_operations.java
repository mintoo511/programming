public class math_operations {
    public static void main(String args[]){

        int input1;
        int input2;
        int product;
        int sum;
        int difference;
        double quotient;
        int remainder;

        input1 = 10;
        input2 = 5;

        product = input1*input2;
        sum = input1+input2;
        difference = input1 - input2;
        quotient = input1/input2;
        remainder = input1%input2;


        System.out.print("Input 1 = "+input1);
        System.out.print("\nInput 2 = "+input2);
        System.out.print("\nProduct = "+product);
        System.out.print("\nSum = "+sum);
        System.out.print("\nDifference = "+difference);
        System.out.print("\nQuotient = "+quotient);
        System.out.print("\nRemainder = "+remainder);

    }
}
