def main():
    a = int(input("Enter your first number: "))
    b = int(input("Enter your second number: "))
    add = a + b
    product = a * b
    minus = a - b
    quotient = a / b
    print("The sum of the two numbers is: " + str(add))
    print("The product of the two numbers is: " + str(product))
    print("The difference between the two numbers is: " + str(minus))
    print("The quotient of the two numbers is: " + str(quotient))
    
    
main()
