// WHILE LOOP
// -Designed to loop an indefinite amount of times, keeps looping WHILE condition is true or UNTIL condition
// is false
// -Use when we do't know how many times to run loops
// while ([condition]) {
//   [loop body]
// }

let countdown = 10;

while (countdown > 0) {
  console.log(--countdown);
}

